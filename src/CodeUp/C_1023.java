package CodeUp;

import java.util.Scanner;

public class C_1023 {
    public static void main(String[] args) {
        //입력된 실수를 정수, 실수부로 나눠 출력
        Scanner sc = new Scanner(System.in).useDelimiter("[.\n]");
        int inum = sc.nextInt();
        int fnum = sc.nextInt();

        System.out.println(inum);
        System.out.println(fnum);

    }
}
